#!/usr/bin/env node
"use strict";

const fs = require("node:fs");
const path = require("node:path");

const REPO_ROOT = path.resolve(__dirname, "../..");
const DEFAULT_OUT = path.join(REPO_ROOT, "dist/selection-runtime.browser.mjs");

function parseArgs(argv) {
  const options = { check: false, artifact: DEFAULT_OUT };
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--check") {
      options.check = true;
    } else if (arg === "--artifact") {
      const value = argv[index + 1];
      if (!value) throw new Error("--artifact benoetigt einen Dateipfad.");
      options.artifact = path.resolve(process.cwd(), value);
      index += 1;
    } else {
      throw new Error(`Unbekanntes Argument: ${arg}`);
    }
  }
  return options;
}

function read(relativePath) {
  return fs.readFileSync(path.join(REPO_ROOT, relativePath), "utf8");
}

function stripCommonJs(source) {
  return source
    .replace(/^"use strict";\s*/u, "")
    .replace(/^const \{[^\n]+\} = require\([^\n]+\);\s*/gmu, "")
    .replace(/\nmodule\.exports = \{[\s\S]*?\};\s*$/u, "\n");
}

function contractConstants() {
  const source = read("src/contracts/selectionTargetContract.js");
  const version = source.match(/const SELECTION_CONTRACT_VERSION = [^;]+;/u)[0];
  const codes = source.match(/const SelectionContractErrorCodes = Object\.freeze\(\{[\s\S]*?\n\}\);/u)[0];
  return `${version}\n\n${codes}\n`;
}

function controllerContract() {
  return stripCommonJs(read("src/contracts/selectionControllerContract.js"))
    .replace(/function createResult/g, "function createSelectionControllerContractResult")
    .replace(/createResult\(/g, "createSelectionControllerContractResult(")
    .replace(/function isObject/g, "function isSelectionControllerContractObject")
    .replace(/isObject\(/g, "isSelectionControllerContractObject(");
}

function createBrowserEsmSource() {
  const sections = [
    "// Generated by scripts/build/build-selection-runtime-browser-esm.cjs. Do not edit by hand.",
    "// Source: src/contracts/selectionTargetContract.js, src/contracts/selectionControllerContract.js, src/selection/*.js",
    contractConstants(),
    controllerContract(),
    stripCommonJs(read("src/selection/targetResolver.js")),
    stripCommonJs(read("src/selection/overlayBase.js")),
    stripCommonJs(read("src/selection/hoverOverlay.js")),
    stripCommonJs(read("src/selection/selectedOverlay.js")),
    stripCommonJs(read("src/selection/selectionController.js")),
    `export {\n  createSelectionController,\n  createHoverOverlay,\n  createSelectedOverlay,\n  resolveSelectionTarget,\n  SelectionRuntimeErrorCodes,\n  SELECTION_CONTRACT_VERSION,\n};\n`,
  ];

  return `${sections.join("\n")}\n`;
}

function checkArtifact(artifactPath, expected) {
  let actual;
  try {
    actual = fs.readFileSync(artifactPath, "utf8");
  } catch (error) {
    console.error(`Browser-ESM-Artefakt fehlt oder ist nicht lesbar: ${path.relative(REPO_ROOT, artifactPath)}`);
    console.error(error.message);
    return 1;
  }

  if (actual !== expected) {
    console.error(`Browser-ESM-Artefakt ist nicht frisch: ${path.relative(REPO_ROOT, artifactPath)}`);
    console.error("Bitte `npm run build:selection-runtime:browser` ausfuehren und das Ergebnis committen.");
    return 1;
  }

  console.log(`Browser-ESM-Artefakt ist aktuell: ${path.relative(REPO_ROOT, artifactPath)}`);
  return 0;
}

function writeArtifact(artifactPath, expected) {
  fs.mkdirSync(path.dirname(artifactPath), { recursive: true });
  fs.writeFileSync(artifactPath, expected, "utf8");
  console.log(`Wrote ${path.relative(REPO_ROOT, artifactPath)}`);
}

function main() {
  const options = parseArgs(process.argv.slice(2));
  const expected = createBrowserEsmSource();

  if (options.check) {
    process.exitCode = checkArtifact(options.artifact, expected);
    return;
  }

  writeArtifact(options.artifact, expected);
}

main();
