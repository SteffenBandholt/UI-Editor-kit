#!/usr/bin/env node
"use strict";

const fs = require("node:fs");
const path = require("node:path");

const REPO_ROOT = path.resolve(__dirname, "../..");
const OUT = path.join(REPO_ROOT, "dist/selection-runtime.browser.mjs");

function read(relativePath) {
  return fs.readFileSync(path.join(REPO_ROOT, relativePath), "utf8");
}

function stripCommonJs(source) {
  return source
    .replace(/^"use strict";\s*/u, "")
    .replace(/^const \{[^\n]+\} = require\([^\n]+\);\s*/gmu, "")
    .replace(/\nmodule\.exports = \{[\s\S]*?\};\s*$/u, "\n");
}

function contractConstants() {
  const source = read("src/contracts/selectionTargetContract.js");
  const version = source.match(/const SELECTION_CONTRACT_VERSION = [^;]+;/u)[0];
  const codes = source.match(/const SelectionContractErrorCodes = Object\.freeze\(\{[\s\S]*?\n\}\);/u)[0];
  return `${version}\n\n${codes}\n`;
}

function controllerContract() {
  return stripCommonJs(read("src/contracts/selectionControllerContract.js"))
    .replace(/function createResult/g, "function createSelectionControllerContractResult")
    .replace(/createResult\(/g, "createSelectionControllerContractResult(")
    .replace(/function isObject/g, "function isSelectionControllerContractObject")
    .replace(/isObject\(/g, "isSelectionControllerContractObject(");
}

const sections = [
  "// Generated by scripts/build/build-selection-runtime-browser-esm.cjs. Do not edit by hand.",
  "// Source: src/contracts/selectionTargetContract.js, src/contracts/selectionControllerContract.js, src/selection/*.js",
  contractConstants(),
  controllerContract(),
  stripCommonJs(read("src/selection/targetResolver.js")),
  stripCommonJs(read("src/selection/overlayBase.js")),
  stripCommonJs(read("src/selection/hoverOverlay.js")),
  stripCommonJs(read("src/selection/selectedOverlay.js")),
  stripCommonJs(read("src/selection/selectionController.js")),
  `export {\n  createSelectionController,\n  createHoverOverlay,\n  createSelectedOverlay,\n  resolveSelectionTarget,\n  SelectionRuntimeErrorCodes,\n  SELECTION_CONTRACT_VERSION,\n};\n`,
];

const output = `${sections.join("\n")}\n`;
fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, output, "utf8");
console.log(`Wrote ${path.relative(REPO_ROOT, OUT)}`);
